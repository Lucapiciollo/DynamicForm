import { InjectionToken, computed, inject } from '@angular/core';
import { patchState, signalStore, StateSignals, withComputed, withHooks, withMethods, withState } from '@ngrx/signals';
import { TypeComboOption } from '../../dynamic-form.interface';

/**************************************************************************************************************************************************/
/**************************************************************************************************************************************************/
/**************************************************************************************************************************************************/
interface IState {
  storeData: { items: Array<any>, totalCount: number },
  filteredOptions: TypeComboOption,
};
/**************************************************************************************************************************************************/
/**************************************************************************************************************************************************/
/**************************************************************************************************************************************************/
const initialState = {
  storeData: null,
  filteredOptions: null
};
/**************************************************************************************************************************************************/
/**************************************************************************************************************************************************/
/**************************************************************************************************************************************************/
const STATE = new InjectionToken<IState>('IState', {
  factory: () => initialState,
});

/**************************************************************************************************************************************************/
/**************************************************************************************************************************************************/
/**************************************************************************************************************************************************/
export const Store = signalStore({ protectedState: false },
  withState(() => inject(STATE)),

  withComputed((store) => ({
    onFilterOption: computed(() => {
      let filtered = store.filteredOptions();
      return filtered;
    }),
  })),


  withMethods((store) => ({
    updateFilterOption(filterd: TypeComboOption): void {
      patchState(store, (state) => ({ ...state, filteredOptions: filterd }));
    },

  })),


  withHooks({
    onInit(store) {
      console.log("**************ComboStoreInitialized**************");
    },
    onDestroy(store) {
      console.log("**************ComboStoreDestroyed**************");
    }
  }),

);



